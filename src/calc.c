#include "calc.h"

int add(int a, int b) {
    int ret = a + b;
    return ret;
}
int sub(int a, int b) {
    int ret = a - b;
    return ret;
}
int mul(int a, int b) {
    int ret = a * b;
    return ret;
}
int div(int a, int b) {
    int ret = a / b;
    return ret;
}
int mod(int a, int b) {
    int ret = a % b;
    return ret;
}
